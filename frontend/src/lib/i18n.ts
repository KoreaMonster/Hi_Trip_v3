export type TranslationKey = keyof typeof translations.en;

export const useTranslations = () => {
  const locale = useLocaleStore((state) => state.locale);
  return useCallback((key: TranslationKey) => translations[locale][key] ?? key, [locale]);
};

export const translate = (key: TranslationKey, locale: Locale) => translations[locale][key] ?? key;
